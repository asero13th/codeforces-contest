def inp():
    return int(input())
def inlt():
    return list(map(int, input().split()))
def insr():
    s = input()
    return list(s[:len(s) - 1])
def invr():
    return map(int, input().split())
def solve():
    n = inp()
    nums = inlt()

    sorted_indices = sorted(range(n), key= lambda i: nums[i])
    target = []
    for i, val in enumerate(sorted_indices):
        if nums[val] < val + 1:
            target.append(val + 1)
    
    
    

    










