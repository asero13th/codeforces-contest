


#        ---- Input Functions ----      #


def inp():
    return int(input())
def inlt():
    return list(map(int, input().split()))
def insr():
    s = input()
    return list(s[:len(s) - 1])
def invr():
    return map(int, input().split())
def solve():
    n = inp()
    solved = input()

    visited = set()
    ans = 0
    for val in solved:
        if val in visited:
            ans += 1
        else:
            visited.add(val)
            ans += 2
    return ans





if __name__ == "__main__":

    t = inp()
    for i in range(t):
        print(solve())

